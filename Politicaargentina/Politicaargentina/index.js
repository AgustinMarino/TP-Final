function politicos(params) {
    let politicos = document.querySelector("#politicos");
    let partidos = document.querySelector("#partidos");
    partidos.style = "display: none";
    politicos.style = "display: block";
}

function partidos(params) {
    let politicos = document.querySelector("#politicos");
    let partidos = document.querySelector("#partidos");
    partidos.style = "display: block";
    politicos.style = "display: none";
}